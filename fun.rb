def greeting(your_name,your_age,your_town,your_favorite_food)
  puts "Hello, #{your_name}!"
  puts "You are #{your_age} years old."
  puts "You live in #{your_town}."
  puts "Your favorite food is #{your_favorite_food}."
end

greeting("Cory","52","Big Lake","toast")
greeting("Mary","52","Big Lake","broccoli")